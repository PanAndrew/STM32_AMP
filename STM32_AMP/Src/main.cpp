void UART_TransmitData(UART_HandleTypeDef *huart, uint8_t *data, uint16_t size)
{

	while(!__HAL_UART_GET_FLAG(huart, UART_FLAG_TXE) || !__HAL_UART_GET_FLAG(huart, UART_FLAG_TC));
	HAL_UART_Transmit_DMA(huart, data, size);
	while(!__HAL_UART_GET_FLAG(huart, UART_FLAG_TC));
}

void sendAllDataFromRB(UART_HandleTypeDef *huart, RINGBUFF_T *ringBuffer)
{
	uint16_t dataSize = RingBuffer_GetCount(ringBuffer);
	if(dataSize)
	{
		uint8_t *msgToSend = new uint8_t[dataSize * sizeof(uint8_t)];
		RingBuffer_PopMult(ringBuffer, msgToSend, dataSize);
		UART_TransmitData(huart, msgToSend, dataSize);
		delete [] msgToSend;
		msgToSend = NULL;
	}
}

